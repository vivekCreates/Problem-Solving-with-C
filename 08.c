// 8. WAP to understand the concept of relational operators (<, <=,>,>=, =, ==).
#include <stdio.h>

int main(){
    int a = 8;
    int b = 4;


    printf("a > b : %d\n",a>b);
    printf("a < b : %d\n",a<b);
    printf("a <=b : %d\n",a<=b);
    printf("a >=b : %d\n",a>=b);
    printf("a ==b : %d\n",a ==b);
    printf("a !=b : %d\n",a!=b);

    return 0;
}